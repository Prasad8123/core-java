class Toys
{
static String name;
static String color;
static int price;

 
static void setName(String nm )
{
  name=nm;
  }
  
  static String getName()
  {
  return name;
  }
  
  static void setColor(String color)
  {
  color=color;
  }
  
  static String getColor()
  {
  return color;
  }
  
  static void setPrice(int productPrice)
  {
  
  price=productPrice;
  }
  
  static int getPrice()
  {
  
  return price;
  }
  }